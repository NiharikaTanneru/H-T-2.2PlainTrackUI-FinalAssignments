// Arrow Function (Multi Parameter,multi line code)
let Employee = (name, age, gender) => {
  console.log(
    "Employee name is:" + name + " " + "age:" + age + " " + "Gender:" + gender
  );
};
Employee("Varun", 38, "Male");

// Arrow Function with No parameter, single line code

let Fruits = () => console.log("Fruits are good for health");
Fruits();

// Arrow function with One parameter, single line code

let vegetables = (value) => console.log("vegetables are " + value);

vegetables("fresh");
